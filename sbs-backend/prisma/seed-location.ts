async function main() {
  console.log('🌱 Seeding location data…');
  let stateCount = 0, cityCount = 0, pincodeCount = 0;

  for (const [stateName, cities] of Object.entries(LOCATION_DATA)) {
    const state = await prisma.state.upsert({
      where: { name: stateName },
      create: { name: stateName },
      update: {},
    });
    stateCount++;

    for (const { city: cityName, pincodes } of cities) {
      // Check if city already exists for this state
      let city = await prisma.city.findFirst({
        where: { name: cityName, stateId: state.id },
      });
      if (!city) {
        city = await prisma.city.create({ data: { name: cityName, stateId: state.id } });
      }
      cityCount++;

      for (const code of pincodes) {
        const exists = await prisma.pincode.findFirst({ where: { code, cityId: city.id } });
        if (!exists) {
          await prisma.pincode.create({ data: { code, cityId: city.id } });
          pincodeCount++;
        }
      }
    }
    console.log(`  ✓ ${stateName} — ${cities.length} cities`);
  }

  console.log(`\n✅ Done! Seeded ${stateCount} states, ${cityCount} cities, ${pincodeCount} pincodes.`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());